-- ROOT 아이템을 찾아 아이템 ID(ITEM_ID), 아이템 명(ITEM_NAME)을 출력하는 SQL문을 작성해 주세요. 이때, 결과는 아이템 ID를 기준으로 오름차순 정렬해 주세요.
SELECT B.ITEM_ID, A.ITEM_NAME
FROM (SELECT ITEM_ID, ITEM_NAME FROM ITEM_INFO) AS A INNER JOIN (SELECT * FROM ITEM_TREE WHERE PARENT_ITEM_ID IS NULL) AS B 
ON A.ITEM_ID=B.ITEM_ID

-- 더 이상 업그레이드할 수 없는 아이템의 아이템 ID(ITEM_ID), 아이템 명(ITEM_NAME), 아이템의 희귀도(RARITY)를 출력하는 SQL 문을 작성해 주세요. 이때 결과는 아이템 ID를 기준으로 내림차순 정렬해 주세요.
-- Null 값이 포함되어 있다면 NOT IN이 정상적으로 작동되지 않을 수 있다.
SELECT ITEM_ID, ITEM_NAME, RARITY 
FROM ITEM_INFO 
WHERE ITEM_ID NOT IN (SELECT DISTINCT(PARENT_ITEM_ID) FROM ITEM_TREE WHERE PARENT_ITEM_ID IS NOT NULL) 
ORDER BY ITEM_ID DESC